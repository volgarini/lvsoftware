    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            produto.setDescricao(jTextField1.getText().trim());
            produto.setCodigoBarras(jTextField2.getText().trim());
            produto.setValor(Float.valueOf(jNumericField1.getText()));
            produto.setQuantidade((Integer) jSpinner1.getValue());
            produto.setTipo(jComboBox1.getSelectedIndex() == 0 ? "P" : "S");

            if (validarProduto()) {
                int id = -1;
                if (produto.getId() > 0) {
                    id = produto.getId();
                    produtosModel.atualizar(produto);
                } else {
                    id = produtosModel.inserir(produto);
                }

                if (id > 0) {
                    super.avisar("Dados salvos com sucesso!");
                } else {
                    super.avisar("Erro ao cadastrar o cliente!");
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            super.avisar("Ocorreu um erro no banco de dados!");
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

