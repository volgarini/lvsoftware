    private void tabelaBusca() {
        String descricao = jTextField1.getText().trim();
        try {
            if (!descricao.isEmpty()) {
                produtosArray = produtosModel.likeDescricao(descricao);
            } else {
                produtosArray = produtosModel.getAll();
            }

            if (produtosArray.size() > 0) {
                TabelaBusca tabelaCliente = new TabelaBusca(this, null, produtosArray, null);
                JOptionPane.getDesktopPaneForComponent(this).add(tabelaCliente);
                tabelaCliente.show();
            } else {
                super.avisar("Nenhum registro encontrado!");
            }
        } catch (SQLException ex) {

        } catch (ClassNotFoundException ex) {

        }
    }

    private boolean validarProduto() throws SQLException, ClassNotFoundException {
        if (produto.getDescricao().isEmpty()) {
            super.avisar("Preencha a descrição do produto/serviço!");
            return false;
        }

        if (produto.getValor() < 0) {
            super.avisar("Valor não pode ser negativo!");
            return false;
        }
        Produtos proTemp = produtosModel.byDescricao(produto.getDescricao());
        if (produto.getId() == -1 && proTemp.getId() > 0) {
            super.avisar("O produto: " + proTemp.getDescricao().trim() + " já está cadastrado");
            return false;
        }

        return true;
    }

    private void limparCampos() {
        jTextField1.setText("");
        jTextField2.setText("");
        jSpinner1.setValue(0);
        jNumericField1.setText("0.00");
        jComboBox1.setSelectedIndex(0);

        produto.setId(-1);
        linha = -1;
        jButton1.setText("Cadastrar");
        jButton3.setVisible(false);
    }

    private void fillCampos() {
        if (linha > -1 && produtosArray.size() > 0) {
            produto = produtosArray.get(linha);
            jTextField1.setText(produto.getDescricao());
            jTextField2.setText(produto.getCodigoBarras());
            jNumericField1.setFloat(produto.getValor());
            jSpinner1.setValue(produto.getQuantidade());
            
            jComboBox1.setSelectedIndex(produto.getTipo().compareToIgnoreCase("P") == 0 ? 0 : 1);
            
            jButton1.setText("Atualizar");
            jButton3.setVisible(true);
            linha = -1;
            produtosArray.clear();
        }
    }

